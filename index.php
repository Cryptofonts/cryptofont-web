        <div class="container">
          <h1 class="mb-4">Get Started</h1>
          <p>Grap a copy of the latest version of Crypto Fonts and use it alongside your source code.</p>
          <div class="row mt-5">
            <div class="col-md-6">
              <h4>1. Move the CSS and web fonts into your project.</h4>
              <p>Copy the entire  <i class="fad fa-folder"></i> <strong>webfonts</strong> folder and the  <i class="fad fa-file"></i> <strong>cryptofont.css</strong> into your project’s static assets directory (or where ever you prefer to keep front end assets or vendor stuff).</p>
            </div>
            <div class="col-md-6 text-center">
              <img src="img/icon-transfer.svg">
            </div>
          </div>

          <div class="row mt-5">
            <div class="col-md-6">
              <h4>2. Reference the CSS</h4>
              <p>Add a reference to the  <i class="fad fa-file"></i> <strong>cryptofont.css</strong> styling file into the <code>&lt;head&gt;</code> of each template or page that you want to use Crypto Font on.
              Pay attention to the pathing of your project and where you moved the files to in the previous step.</p>
            </div>
            <div class="col-md-6">
              <div class="card terminal">
                <div class="card-header">
                  <ul class="list-inline">
                    <li class="dot"></li>
                    <li class="dot"></li>
                    <li class="dot"></li>
                    <li class="white float-right"><i class="fab fa-html5"></i> index.html</li>
                  </ul>
                </div>
                <div class="card-body">
                  <p>
                    &lt;<span class="markup">head</span>&gt;<br>
                    &lt;!--CryptoFont--&gt;<br>
                       &lt;<span class="markup">link</span> <span class="ref">href=</span><span class="link">"cryptofont.css"</span><span class="ref">rel=</span><span class="link">"stylesheet"</span>&gt;<br>
                    &lt;<span class="markup">/head</span>&gt;
                  </p>
                </div>
              </div>
            </div>
          </div>

          <div class="row mt-5">
            <div class="col-md-6">
              <h4>3. Place icons in your UI’s markup</h4>
              <p>With the references complete, you can now start placing icons in your HTML’s <code>&lt;body&gt;</code>
              We recommend using a consistent HTML element, like an <code>&lt;i&gt;</code> You can place Crypto Fonts icons just about anywhere using the CSS Prefix cf and the crypto ticker.</p>
            </div>
